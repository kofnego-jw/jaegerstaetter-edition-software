package at.kulinz.jaegerstaetter.xslt.extend;

public class XsltExtendException extends Exception {

    public XsltExtendException() {
    }

    public XsltExtendException(String message) {
        super(message);
    }

    public XsltExtendException(String message, Throwable cause) {
        super(message, cause);
    }

    public XsltExtendException(Throwable cause) {
        super(cause);
    }
}
